// intalization 
void Init(void)
{ 
	int i;                         	
  for(i=0;i<5;i++)   	// loop to 5 shapes to determine x ,y cordinate,number of points and speed 
  {
    enemy[i].x = rand()%62;               // random x cordinate between (0,61)
    enemy[i].y = ENEMY10H-2;              // at the start of screen
		enemy[i].points = rand()%7+1;         // number of points between (1,7)
		enemy[i].speed = rand()%3+1;          // speed value betwwn (1,3)
		enemy[i].live=0;                      // at first all of enemies died
  }
	
                                  // determine player data
	player.image=PlayerShip0;               // shape of player
	player.x=30;                            //position of player
	player.y=47;
	
	
	
	enemy[0].image=SmallEnemy10PointA;        // intalize their image kind
	enemy[1].image=SmallEnemy20PointA;
	enemy[2].image=SmallEnemy30PointA;
	enemy[3].image=SmallEnemy10PointB;
	enemy[4].image=SmallEnemy20PointB;
	
	
	
	score=0;               
	flag1=0;
	flag2=0;

	
}
void moveDown(int i)                        // take step to down 
{ 
  enemy[i].y+=enemy[i].speed;                // move with enemy speed
}

void moveRight(void)                       // take step right        
{
	player.x+=4;                            
	flag1=1;                                 // SW1 had been clicked
}
void moveLeft(void)                        // take step to left
{
	player.x-=4;
	flag2=1;                                 // SW2 had been clicked
}


