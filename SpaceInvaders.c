void enemyMovment(void)                // if enemy is alive move it down and draw it in buffer
{
	int i;
	for(i=0;i<5;++i)
	{
		if(enemy[i].live)
		{
			moveDown(i);
			drawEnemy(i);
		}
	}
}

                                 
void drawNewEnemy(int i)                               // when enemy die it appear as new one from screen start with random x position
{                                                        // reuse the 5 enimes when one is died as new one
	enemy[i].y = ENEMY10H-2;                              //at start of screen
	enemy[i].x = rand()%60;                                // new x position
	enemy[i].live=1;                                     
	Nokia5110_PrintBMP(enemy[i].x, enemy[i].y, enemy[i].image, 0);
}

void isEnemyDie(void)                                    // detect if the player pass an enemy when enemy pass the screen , increase the score 
{                                                          // and draw new enemy
 int i;
	for(i=0;i<5;++i)
	{
		if(enemy[i].live && enemy[i].y>47)                  // if enemy pass the screen it is dead
		{
			score+=enemy[i].points;                           // add enemy points to score
			enemy[i].live=0;
			drawNewEnemy(i);                                  // draw new enemy
		}
	
	}
}

int isPlayerDie(void)                                 //detect the player die when a enemy touch the player 
{
	int i;
	for(i=0;i<5;++i)
	{
		 if(enemy[i].live)
		 {
			        // they are touching when have same y and the enemy x cordinate between the player width
			  if(enemy[i].y==player.y && enemy[i].x>=player.x && enemy[i].x<=player.x+(ENEMY10W-2))return 1;
		 }
	}
	return 0;
}



